package org.starmx.jmx.mbeanserver;

public class MBeanServerConnectionException extends Exception {

	public MBeanServerConnectionException() {
		super();
	}

	public MBeanServerConnectionException(String message, Throwable cause) {
		super(message, cause);
	}

	public MBeanServerConnectionException(String message) {
		super(message);
	}

	public MBeanServerConnectionException(Throwable cause) {
		super(cause);
	}

}
